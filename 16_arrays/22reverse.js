// METHOD - reverse

// reverse order of array
// can be used on any kind of array
// arr.reverse()
// returns reversed array
// original array modified


const arr1 = ['e', 'g', 'b', 'c', 'a']
const arr2 = ["tomato", "potato", "turmeric"]
const arr3 = [10, 5, 34, 8]
const arr4 = ['a1','1a','3c']

console.log(arr1.reverse())             // [ 'a', 'c', 'b', 'g', 'e' ]
console.log(arr2.reverse())             // [ 'turmeric', 'potato', 'tomato' ]
console.log(arr3.reverse())             // [ 8, 34, 5, 10 ]
console.log(arr4.reverse())             // [ '3c', '1a', 'a1' ]