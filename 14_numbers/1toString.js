// Method - toString
// Number.toString()
// returns a string

const num1 = 23
const num2 = new Number(46)

const str1 = num1.toString()
const str2 = num2.toString()   

console.log(`str1: ${str1} type: ${typeof str1}`)       // str1: "23" type: string
console.log(`str2: ${str2} type: ${typeof str2}`)       // str2: "46" type: string
